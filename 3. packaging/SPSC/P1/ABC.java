package P1;
public class ABC
{
	private int a = 10;
		int b = 20; //default
	protected int c = 30;
	public int d = 40;
	public void show ()
	{
		System.out.println("Private A : "+a);
		System.out.println("Default B : "+b);
		System.out.println("Protected C : "+c);
		System.out.println("Public D : "+d);
	}
}
